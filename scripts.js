document.addEventListener('alpine:init', () => {
   Alpine.data('game', () => ({
      set: '',
      list: [],
      pick: {},
      discarded: [],
      status: 'notStarted',
      locked: false,
      remaining: -1,
      pickSelect: -1,
      showModal: true,

      init() {
         window.onbeforeunload = () => 'Are you sure?'
         console.log('By https://fagnerjb.com')

         if (!location.hash) {
            return
         }

         let theHash = location.hash
         if ('#set:' !== theHash.slice(0, 5)) {
            return
         }

         let set = theHash.slice(5)
         if (!Object.keys(characters).includes(set)) {
            return
         }

         this.setList(set)
      },

      setList(set) {
         this.set = set
         this.showModal = false
         location.hash = `#set:${this.set}`
         setTimeout(() => (this.list = characters[this.set].list), 1)

         this.remaining = this.list.length
         this.pick = {}
         this.discarded = []
         this.status = 'notStarted'
         this.locked = false
         this.pickSelect = -1
      },

      getTitle() {
         if (this.set.length === 0) {
            return document.title
         }

         return `${document.title} with ${characters[this.set].name}`
      },

      restartGame() {
         const confirmRestart = window.confirm('Are you sure?')

         if (!confirmRestart) {
            return
         }

         this.setList(this.set)
      },

      updateGame() {
         this.remaining = this.list.length - this.discarded.length

         if (typeof this.list[this.pickSelect] === 'undefined') {
            this.pick = {}
            return
         }

         this.pick = this.list[this.pickSelect]
      },

      toggleItemStatus(key) {
         if (this.remaining === 1) {
            return
         }

         if (this.discarded.includes(key)) {
            this.discarded.splice(this.discarded.indexOf(key), 1)
         } else {
            this.discarded.push(key)
         }
      },

      getItemStatus(key) {
         return !this.discarded.includes(key)
      },
   }))
})
